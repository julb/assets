package io.julb.library.utility.constants;

/**
 * A class to list constants in Chars format.
 * <P>
 * @author Julb.
 */
public class Chars {

    /**
     * The '0' character.
     */
    public static final char ZERO = '0';

    /**
     * The '9' character.
     */
    public static final char NINE = '9';

    /**
     * The 'a' character.
     */
    public static final char A_LOWERCASE = 'a';

    /**
     * The 'f' character.
     */
    public static final char F_LOWERCASE = 'f';

    /**
     * The 's' character.
     */
    public static final char S_LOWERCASE = 's';

    /**
     * The 'v' character.
     */
    public static final char V_LOWERCASE = 'v';

    /**
     * The 'z' character.
     */
    public static final char Z_LOWERCASE = 'z';

    /**
     * The "/" character.
     */
    public static final char SLASH = '/';

    /**
     * The "-" character.
     */
    public static final char DASH = '-';

    /**
     * The "." character.
     */
    public static final char DOT = '.';

    /**
     * The "," character.
     */
    public static final char COMMA = ',';

    /**
     * The "*" character.
     */
    public static final char STAR = '*';

    /**
     * The ":" character.
     */
    public static final char SEMICOLON = ':';

    /**
     * The "|" character.
     */
    public static final char PIPE = '|';

    /**
     * The " " character.
     */
    public static final char SPACE = ' ';

    /**
     * The "=" character.
     */
    public static final char EQUAL = '=';

    /**
     * The "&amp;" character.
     */
    public static final char AND = '&';

    /**
     * The quotation character.
     */
    public static final char QUOTATION_MARK = '"';

    /**
     * The question mark character.
     */
    public static final char QUESTION_MARK = '?';

    /**
     * The exclamation mark character.
     */
    public static final char EXCLAMATION_MARK = '!';

}
